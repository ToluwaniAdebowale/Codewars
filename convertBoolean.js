// Complete the method that takes a boolean value and return a "Yes" string for true, or a "No" string for false.

// return "Yes" if the boolean is true
// return "No" if the boolean is false

function boolToWord( bool ){
  if (bool === true){
    return "Yes";
  }
  return "No";
}
